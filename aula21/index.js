/* 
OPERADORES LÓGICOS
&& -> AND -> E -> todas as expressões precisam ser verdadeiras para retornar true 
|| -> OR -> OU -> todas as expressões precisam ser falsas para retornar false
! -> NOT -> NÃO -> 
*/
// operador AND
const expressaoAnd = true && true && false && true;

// operador OR
const expressoaOr = true || false;
console.log(expressoaOr)

// operador NOT
console.log(!true);
console.log(!false);